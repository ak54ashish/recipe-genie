export type IndianCategory = "all" | "veg" | "non-veg" | "snacks" | "north-indian" | "south-indian" | "drinks";

export function filterIndianRecipes(recipes: IndianRecipe[], category: IndianCategory): IndianRecipe[] {
  switch (category) {
    case "veg":
      return recipes.filter((r) => r.category === "veg");
    case "non-veg":
      return recipes.filter((r) => r.category === "non-veg");
    case "snacks":
      return recipes.filter((r) => r.type === "snack");
    case "north-indian":
      return recipes.filter((r) => r.region === "North Indian");
    case "south-indian":
      return recipes.filter((r) => r.region === "South Indian");
    case "drinks":
      return recipes.filter((r) => r.type === "drink");
    case "all":
    default:
      return recipes;
  }
}
